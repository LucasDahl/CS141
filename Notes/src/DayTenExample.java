public class DayTenExample {

    public static void main(String[] args) {

        // Properties
        int x = 8;

        if(x < 10) {

            System.out.println("This line sometimes happens");

        } else if (x > 12) {

            System.out.println("This line sometimes happens as well");

        } else {

            System.out.println("This line sometimes happens");

        }

        System.out.println("This line ALWAYS happens");

    }

}
